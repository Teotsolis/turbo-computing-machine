    private void actualizarTablaDent(){
        Dentista Dent = new Dentista();
        ArrayList<Dentista> Select = Dent.getDentistas();
        DefaultTableModel Temp = new DefaultTableModel();
        Temp.addColumn("ID");
        Temp.addColumn("Nombre");
        Temp.addColumn("Apellidos");
        Temp.addColumn("Especialidad");
        Temp.addColumn("Telefono");
        Temp.addColumn("Celular");
        Temp.addColumn("Dias");
        for(int i = 0; i  < Select.size(); i++){
            Temp.addRow(Dent.RowDent(Select.get(i)));
        }
        tableDataDent.setModel(Temp);
    }
    
    
